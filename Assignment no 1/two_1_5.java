public class two_1_5 
{
    public static void main(String[] args)
    {
        int a=40, b=17;
        System.out.println("a&b="+(a&b));
        System.out.println("a|b="+(a|b));
        System.out.println("a^b="+(a^b));
        System.out.println("~b="+(~b));
        System.out.println("a<<2="+(a<<2));
        System.out.println("a>>2="+(a>>2));
        System.out.println("a>>>2="+(a>>>2));

    }
    
}
