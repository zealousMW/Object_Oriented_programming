import java.io.*;
public class one_2 {
    static int aa;
    static byte bb;
    static short cc;
    static float dd;
    static long ee;
    static double ff;
    static boolean gg;
    static char hh;
    public static void main(String[] args)
    {
        System.out.println("The Default Value of All Primitive Datay Types are as follows:");
        System.out.println("\t-> Integer\t"+aa);
        System.out.println("\t-> Short\t\t"+cc);
        System.out.println("\t-> Float\t\t"+dd);
        System.out.println("\t-> Byte\t\t"+bb);
        System.out.println("\t-> Long\t\t"+ee);
        System.out.println("\t-> Double\t"+ff);
        System.out.println("\t-> Boolean\t"+gg);
        System.out.println("\t-> Character\t"+hh);
    }
}
